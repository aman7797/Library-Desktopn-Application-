    @Override
    public void clearFields() {
        if (employeeComboBox.getSelectedItem() == null) {
            if (table.getModel() != null && table.getModel() instanceof ContractTableModel) {
                ContractTableModel model = (ContractTableModel) table.getModel();
                model.clear();
            }
        }
        startDateChooser.setDate(null);
        endDateChooser.setDate(null);
        activeCheckBox.setSelected(true);
    }

    @Override
    public void showSelectedItem(Object item) {
        Contract contract = (Contract) item;
        if (contract != null) {
            employeeComboBox.setSelectedItem(contract.getEmployee());
            startDateChooser.setDate(contract.getStartDate());
            endDateChooser.setDate(contract.getEndDate());
            activeCheckBox.setSelected(contract.getActive());
        }
    }

    @Override
    public void flesh(Object item) {
        Contract contract = (Contract) item;
        if (contract != null) {
            contract.setEmployee((Employee) employeeComboBox.getSelectedItem());
            contract.setStartDate(startDateChooser.getDate());
            contract.setEndDate(endDateChooser.getDate());
            contract.setActive(activeCheckBox.isSelected());
        }
    }

    private class ContractTableModel extends LeaveManagerTableModel {

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            if (rowIndex >= getRows().size()) {
                return null;
            }
            Contract contract = (Contract) getRow(rowIndex);
            switch (columnIndex) {
                case 0:
                    return contract.getStartDate();
                case 1:
                    return contract.getEndDate();
                case 2:
                    return contract.getActive();
                default:
                    return null;
            }
        }

        @Override
        public void createRow(Object row) {
            Contract newContract = (Contract) row;
            if (!getRows().contains(row)) {
                if (!getRows().isEmpty()) {
                    boolean inserted = false;
                    for (Object item : getRows()) {
                        Contract contract = (Contract) item;
                        if (newContract.compareTo(contract) == 1) {
                            int rowIndex = getRows().indexOf(item);
                            getRows().add(rowIndex, row);
                            inserted = true;
                            fireTableRowsInserted(rowIndex, rowIndex);
                            break;
                        }
                    }
                    if (!inserted) {
                        super.createRow(row);
                    }
                } else {
                    super.createRow(row);
                }
            }
        }

        @Override
        public void editRow(Object row) {
            super.destroyRow(row);
            this.createRow(row);
        }

        @Override
        public String[] getColumns() {
            String[] columns = {"Start Date", "End Date", "Active"};
            return columns;
        }
    }
